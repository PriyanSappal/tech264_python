def add(x, y):
    result = x + y
    print(f"The result of adding {x} and {y} is: {result}")

def subtract(x, y):
    result = x - y
    print(f"The result of subtracting {y} from {x} is: {result}")

def multiply(x, y):
    result = x * y
    print(f"The result of multiplying {x} and {y} is: {result}")

def divide(x, y):
    result = x / y
    print(f"The result of dividing {x} by {y} is: {result}")
